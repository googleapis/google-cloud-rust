package language

import (
	"errors"
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/cbroglie/mustache"
	"github.com/googleapis/google-cloud-rust/generator/internal/api"
)

type mustacheProvider struct {
	impl    func(string) (string, error)
	dirname string
}

func (p *mustacheProvider) Get(name string) (string, error) {
	if strings.HasPrefix(name, "/") {
		return p.impl(strings.TrimPrefix(name, "/") + ".mustache")
	}
	return p.impl(filepath.Join(p.dirname, name) + ".mustache")
}

// GenerateFromModel generates a number of files always using the api.API model
// as the input to the mustache template.
//
// This works well for languages like Go and Rust, where a single file can
// contain all the clients or messages generated by an API. We anticipate that
// other functions will be needed for languages like Java or C++, where it is
// conventional to have a single class per file.
func GenerateFromModel(outdir string, model *api.API, provider TemplateProvider, generatedFiles []GeneratedFile) error {
	var errs []error
	for _, gen := range generatedFiles {
		templateContents, err := provider(gen.TemplatePath)
		if err != nil {
			errs = append(errs, err)
			continue
		}
		if outdir == "" {
			wd, _ := os.Getwd()
			outdir = wd
		}
		destination := filepath.Join(outdir, gen.OutputPath)
		os.MkdirAll(filepath.Dir(destination), 0777) // Ignore errors
		nestedProvider := mustacheProvider{
			impl:    provider,
			dirname: filepath.Dir(gen.TemplatePath),
		}
		s, err := mustache.RenderPartials(templateContents, &nestedProvider, model)
		if err != nil {
			errs = append(errs, err)
			continue
		}
		if err := os.WriteFile(destination, []byte(s), os.ModePerm); err != nil {
			errs = append(errs, err)
		}
	}
	if len(errs) > 0 {
		return fmt.Errorf("errors generating output files: %w", errors.Join(errs...))
	}
	return nil
}
