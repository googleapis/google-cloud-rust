  /// a metric type and a
  /// `MonitoredResourceDescriptor`, that
  /// is associated with this metric type can only be associated with one of the
  /// monitored resource types listed here.
  final List<String>? monitoredResourceTypes;

  MetricDescriptor({
    this.name,
    this.type,
    this.labels,
